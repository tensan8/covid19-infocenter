                                        <p align="justify">The Sinovac Vaccine and sold under the brand name CoronaVac is an inactivated vaccine for COVID-19 developed by the Beijing-based biopharmaceutical company Sinovac.
                                        
                                        The Sinovac vaccine has received approval from China's National Medical Products Administration (NMPA) to conduct Phase I/II human clinical trials in China as of April 13, 2020, to determine the vaccine candidate's safety, tolerance, dosage, and immunisation schedule.
                                        
                                        On February 3, 2021, <a href = "https://www.thelancet.com/journals/laninf/article/PIIS1473-3099(20)30987-7/fulltext"><em>The Lancet</em></a> published a study that concluded 'CoronaVac was well tolerated and induced humoral responses in adults aged 60 years and older, which supports this vaccine's use in an older population.'
                                        </p>
                                            <p>Source(s): <a href="https://www.bbc.com/news/world-asia-china-55212787">www.bbc.com</a>, <a href ="https://www.precisionvaccinations.com/vaccines/coronavac-covid-19-vaccine">precisionvaccinations.com</a>
                                            </p>
                                            
                                        <h2 class="card-title h3 mb-3 text-left">How does this vaccine work?</h2>
                                        <p align="justify">It works by using dormant/deactivated viral particles and injecting them into the body and exposing the deactivated viral strands to the body without triggering a severe immune response.
                                        
                                        In essence, inactivated vaccines use a pathogen that has been modified so that it cannot replicate to stimulate our immune system.
                                        </p>
                                        <p>Source(s): <a href=https://www.bbc.com/news/world-asia-china-55212787>www.bbc.com</a>
                                        </p>
