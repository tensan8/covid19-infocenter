                                        <h2 class="card-title h3 mb-3 text-left">How effective is the Sinovac Vaccine?</h2>
                                        <p align="justify">As of currently, it is still unclear to place an exact percentage of efficacy on the Sinovac vaccine as various trials from other countries showed different results.
                                        
                                        CoronaVac has been undergoing phase three trials in various countries. Interim data from late-stage trials in Turkey and Indonesia showed that the vaccine was 91.25% and 65.3% effective respectively. Researchers in Brazil initially said it was 78% effective in their clinical trials, but in January 2021 revised that figure to 50.4% after including more data in their calculations.
                                        
                                        Meanwhile, the vaccine reported 79% efficacy in an interim evaluation.
                                        
                                        As of April 6 2021, Researchers from Turkey reported CoronaVac's immunity rate was 25.3% after 28 days of the first dose and 97.92% after 28 days upon receiving the second vaccine dose. The effective antibody response was found to be 29.5% in women after the first vaccine; 18.6% in males.
                                        </p>
                                        <p>Source: <a href="https://www.bbc.com/news/health-55302595">www.bbc.com</a>
                                        </p>
                                        <h2 class="card-title h3 mb-3 text-left">Does the vaccine protect against new variants?</h2>
                                        <p  align="justify">Preliminary results from a large study of healthcare workers suggest that one dose of CoronaVac is still about 50% effective against symptomatic COVID-19 in a Brazilian city where more than three-fourths of new cases are caused by the highly transmissable variant known as P.1.
                                        
                                        The 49.6% real-world effectiveness is similar to the vaccine's efficacy of 50.34% against symptomatic COVID-19 after both doses, found in a phase 3 clinical trial conducted in Brazil by the Butantan Institute, a state-owned research institute and vaccinemaker.
                                        </p>
                                        <p>Source: <a href="https://www.sciencemag.org/news/2021/04/chinese-covid-19-vaccine-maintains-protection-variant-plagued-brazil">sciencemag.org</a>
                                        </p>
